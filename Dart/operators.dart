void main() {
  int a = 10, b = 3;
  print(a + b);
  print(a - b);
  print(a * b);
  print(a / b);
  print(a % b);

  // rELATIONAL oPERATOR
  int age = 18;
  print(age == 18);
  print(age != 18);
  print(age >= 18);

  // Logical Operator
  bool isStudent = true;
  bool isAdult = false;

  print(isStudent && isAdult);
  print(isStudent || isAdult);
}
